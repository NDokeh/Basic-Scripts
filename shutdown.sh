#!/bin/bash

#The following is a shutdown script for linux systems

#Shutdown will commence at 10 minutes (+10), it can be set for 5 minutes(+5) or 15 minutes (+15)

shutdown -r +10 "system will go down in 10 minutes"

sleep 300

wall System will go down in 5 minutes. Drop whatever you're doing and save 
your work.

sleep 240

wall Final One minute warning: System will shut down in one minute.
